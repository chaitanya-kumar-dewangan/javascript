
// while loop
let index = 0;

while (index <= 10) {
    console.log(`value of index is ${index}`);
    index = index + 2;
}



// do while loop
// executes the block atleast once
console.log("do while loop")

let score = 1;

do {
    console.log(`here score is  ${score}`)
    score += 1;
} while (score <= 10)