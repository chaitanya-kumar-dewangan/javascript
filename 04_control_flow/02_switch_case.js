const month = 3;
switch (month) {
    case 1:
        console.log("this is january")

        break;
    case 2:
        console.log("this is february")

        break;
    case 3:
        console.log("this is March")

        break;
    case 4:
        console.log("this is April")

        break;
    case 5:
        console.log("this is May")

        break;

    default:
        console.log("not exists")
        break;
}

